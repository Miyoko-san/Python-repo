# len()
name = "Miyoko"
print(len(name))


#.endswith()  yields True/False
print(name.endswith("oko")) #True in this case


#.startswith() yields True or False
#Last two fns are case sensitive
print(name.startswith("mi")) # False in this case as small 'm' is used

# .capitalize()
print(name.capitalize()) #Capitalizes first character only


# Note : All these fns on strings create a new string when asked to print in terminal 
